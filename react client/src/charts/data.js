export const PolarChartFilter = (Data) => {
    function uniqueWithOccr(array) {
        const uniqueArray = [];
        const occrMap = {};
        // Count occurrences of each element
        array.forEach(element => {
            if (!occrMap[element.name]) {
                occrMap[element.name] = { occr: 1, id: element._id };
            } else {
                occrMap[element.name] = { occr: occrMap[element.name].occr + 1, id: element._id }
            }
        });
        // Create new array with unique elements and their occurrences
        Object.keys(occrMap).forEach(element => {
            uniqueArray.push({ name: element, ...occrMap[element] });
        });
        return uniqueArray;
    }
    let mergeArray = [];
    Data.forEach(({ meals: { breakfast, dinner, lunch, snackAm, snackPm } }) => {
        mergeArray = mergeArray.concat(breakfast);
        mergeArray = mergeArray.concat(dinner);
        mergeArray = mergeArray.concat(lunch);
        mergeArray = mergeArray.concat(snackAm);
        mergeArray = mergeArray.concat(snackPm);
    })
    const uniqueArray = uniqueWithOccr(mergeArray);
    return uniqueArray;
}


